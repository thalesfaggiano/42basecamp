int	ft_str_is_alpha(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		if (!((str[i] > 0x40 && str[i] < 0x5b)
				|| (str[i] > 0x60 && str[i] < 0x7b)))
			return (0);
	return (1);
}
